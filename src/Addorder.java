        try
        {
             Class.forName("com.mysql.jdbc.Driver");
             con1 = DriverManager.getConnection("jdbc:mysql://localhost/pcm","root","");
             Statement s=con1.createStatement();
             
             ResultSet rs=s.executeQuery("Select Max(id) from MachineDetails");
             rs.next();
             
             rs.getString("Max(id)");
             
             if(rs.getString("Max(id)")==null)
             {
                 jTextField1.setText("A0001");
                 jTextField2.setText("B0001");
                 jTextField3.setText("J0001");
                 jTextField4.setText("CA0001");
                 
             }
             else
             {
                 Long id=Long.parseLong(rs.getString("Max(id)").substring(2,rs.getString("Max(id)").length()));
                 id++;
                 jTextField1.setText("A0" + String.format("%03d",id));
                 jTextField2.setText("B0" + String.format("%03d",id));
                 jTextField4.setText("CA0" + String.format("%03d",id));
                 jTextField3.setText("J0" + String.format("%03d",id));
                 
             }
             
             
        }
