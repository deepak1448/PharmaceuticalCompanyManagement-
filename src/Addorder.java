            ps.setString(2,model);
            ps.setString(3,job);
            ps.setString(4,serial);
            ps.setString(5,date);
            ps.setString(6,deploying);
            ps.setString(7,name);
            ps.setString(8,company);
            ps.setString(9,type);
            ps.setString(10,dispatch);
            ps.setString(11,payment);
            ps.setString(12,capacity);
            ps.setString(13,function);
            ps.setString(14,material);
            ps.setString(15,quantity);
            ps.setString(16,display);
            ps.setString(17,loading);
            ps.setString(18,size);
            ps.setString(19,status);
